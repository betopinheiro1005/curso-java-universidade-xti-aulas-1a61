package br.com.xti.exemplos;

public class VarRef {

    public static void main(String[] args) {

        // Variavel primitiva
        int x = 7;
        x = 9;

        // Variavel de referencia
        String y = "XTI";
        y = "xti.com.br";

        System.out.println(x);
        System.out.println(y);
    }
}
